#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*newnode;

	newnode = (t_list *)malloc(sizeof(t_list));
	if (!newnode)
		return (newnode = NULL);
	newnode->next = NULL;
	newnode->content = content;
	return (newnode);
}
