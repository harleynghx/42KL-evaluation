#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*rtn;

	rtn = malloc(size * count);
	if (!rtn)
		return (rtn);
	ft_bzero(rtn, (count * size));
	return (rtn);
}
